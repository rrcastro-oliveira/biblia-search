package santes.toni.bibliasearch;

public class BibliaSearcherException extends RuntimeException {

	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;

	public BibliaSearcherException() {
	}

	public BibliaSearcherException(String message) {
		super(message);
	}

	public BibliaSearcherException(Throwable cause) {
		super(cause);
	}

	public BibliaSearcherException(String message, Throwable cause) {
		super(message, cause);
	}

}
